from mos.utils import *
import numpy as np
import cv2

__all__ = [
    'draw_hough_lines'
]


################################################################################
# UTILITIES ====================================================================
################################################################################
def draw_hough_lines(img, lines):
    if lines is not None:
        if len(img.shape) == 2:
            img = cv2.cvtColor(img, cv2.COLOR_GRAY2BGR)

        for line in lines:
            x1, y1, x2, y2 = line[0]
            cv2.line(img,(x1,y1),(x2,y2),(0,255,0),5)

    return img


